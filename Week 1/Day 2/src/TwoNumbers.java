public class TwoNumbers {
    public static void main(String[] args) {

        double a = 22;
        double b = 13;
        System.out.println(13 + 22);
        System.out.println(13 - 22);
        System.out.println(22 * 13);
        System.out.println(a / b);
        System.out.println(22 / 13);
        System.out.println(a % b);






    }
}
